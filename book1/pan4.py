# 小王  90  72  76  93  56
# 李四  81  85  91  89  77
# 張山  79  53  47  94  80

# 刪除
print('--刪除--')
df = df_copy
# 刪除列
print(df.drop('李四'))
# 刪除行
print(df.drop('數學', axis=1))
# 刪除連續列
